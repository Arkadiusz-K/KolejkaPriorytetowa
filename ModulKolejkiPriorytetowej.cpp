void input( PQueue* q, int nSize )
{
	for( int i = 0; i < nSize; i++ )
	{
		int nKey = rand() % 40 + 1;
		int nPrior = rand() % 15 + 1;
		PQEnqueue( q, nKey, nPrior );
		printf( "%d. %d\tprior:%d\n", i, nKey, nPrior );
	}
}



